package Sems1.Assignment_1;

 /* What do each of the following print?
a) System.out.println(5);
b) System.out.println((float)25/6);
c) System.out.println(5+'6');
d) System.out.println(5+7+'9');
e) System.out.println("92"+7+5);
f) System.out.println(2+"9");
 */

public class A1Q9 {
	public static void main(String[] args) {
		System.out.println(5);  //Output: 5
		System.out.println((float)25/6);  //Output: 4.1666665
		System.out.println(5+'6');  //Output: 59
		System.out.println(5+7+'9');    //Output: 69
		System.out.println("92"+7+5);   //Output: 9275
		System.out.println(2+"9");  //Output: 29
	}
}
